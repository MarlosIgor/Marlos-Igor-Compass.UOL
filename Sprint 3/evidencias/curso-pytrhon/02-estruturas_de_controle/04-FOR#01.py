# 1 ate 10
for i in range(1, 11):
    print(f'i = {i}')
print('-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-\n')

# 0 ate 9
for j in range(10):
    print(f'j = {j}')
print('-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-')

# tabuada
for x in range(1, 11):
    print(f'\ntabuada do {x}')
    for y in range(1, 11):
        print(f'{x} * {y} = {x * y}')
